#include<stdio.h>
int main()
{
    char ch[7][7]={{' ',' ',' ','*'},{' ',' ','*','*','*'},{' ','*','*','*','*','*'},{'*','*','*','*','*','*','*'},{' ','*','*','*','*','*'},{' ',' ','*','*','*'},{' ',' ',' ','*'}};
	int i,j;
	for(i=0;i<=6;i++)  //对于组，若最先就给值，只能在后面直接等
	{
		for(j=0;j<=6;j++)
			printf("%c",ch[i][j]); //空字符''之间要空格成' '
		printf("\n");  //换行符的位置要摆正，切记
	}
	return 0;  //对于组，行可省，列不可省
}
